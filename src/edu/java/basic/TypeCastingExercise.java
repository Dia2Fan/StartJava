package edu.java.basic;

public class TypeCastingExercise {
    public static void main(String[] args) {
        char ch = 'A';
        System.out.println("문자 " + ch + "의 아스키코드는 " + (int) ch + "입니다.");

        int code = 66;
        System.out.println("아스키코드 " + code + "의 문자는 " + (char) code + "입니다.");
    }
}
